import SwiftUI
import UIKit

extension Color {
    static let appPrimary           = Color("appPrimary")
    static let appPrimaryDark       = Color("appPrimaryDark")
    static let appGray              = Color("appGray")
    static let appGrayDark          = Color("appGrayDark")
    static let appGrayLight         = Color("appGrayLight")
    static let appBlue              = Color("appBlue")
    static let appGreen             = Color("appGreen")
}

extension UIColor {
    static let appPrimary      = UIColor(named: "appPrimary")!
    static let appPrimaryDark  = UIColor(named: "appPrimaryDark")!
    static let appGray         = UIColor(named: "appGray")!
    static let appGrayDark     = UIColor(named: "appGrayDark")!
    static let appGrayLight    = UIColor(named: "appGrayLight")!
    static let appBlue         = UIColor(named: "appBlue")!
    static let appGreen        = UIColor(named: "appGreen")!
}
